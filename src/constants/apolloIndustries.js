// Helper function to get industry ID by name
export function getIndustryId(industryName) {
  const industry = APOLLO_INDUSTRIES.find(ind => ind.name === industryName);
  return industry ? industry.id : null;
}

// Helper function to get industry name by ID
export function getIndustryName(industryId) {
  const industry = APOLLO_INDUSTRIES.find(ind => ind.id === industryId);
  return industry ? industry.name : null;
}

// Helper function to get multiple industry IDs from names array
export function getIndustryIds(industryNames) {
  return industryNames
    .map(name => getIndustryId(name))
    .filter(id => id !== null);
}
